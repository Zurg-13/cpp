
// Описание структуры Attr
export class Attr {
    id: number;
    name: string;
    note: string;
    type: string;

    constructor(id: number, name: string, note: string, type: string) {
        this.id = id;
        this.name = name;
        this.note = note;
        this.type = type;
    }// constructor

}// Attr
