// Контрастный цвет (чёрный/белый). --------------------------------------------
//------------------------------------------------------------------------------
QColor contrast_bw(const QColor &clr) {
    return (clr.red() + clr.green() + clr.blue() < 127 ? Qt::white : Qt::black);
}// contrast_bw

