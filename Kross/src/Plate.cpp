// INCLUDE. --------------------------------------------------------------------
//------------------------------------------------------------------------------
#include "Plate.h"

// Глобальные переменные. ------------------------------------------------------
//------------------------------------------------------------------------------
extern const QString CLR, RED, GRN, BLU;

/* Плашка. ********************************************************************/
/******************************************************************************/

// Конструктор. ----------------------------------------------------------------
//------------------------------------------------------------------------------
Plate::Plate(int val, QWidget *parent, Qt::WindowFlags f) : QLabel(parent, f) {
    setVal(val);
}

// Задать значение. ------------------------------------------------------------
//------------------------------------------------------------------------------
void Plate::setVal(int val) {
    this->setText(STR(val)); this->val = val;

    setStyleSheet(CLR +
        (this->val == 0 ? BLU
        :this->val >  0 ? GRN : RED) + ";");

}// Plate

